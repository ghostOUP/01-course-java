package Loops;

public class Loops {
    public static void main(String[] args) {

        for (int y = 1; y <= 10; y++) {
            for (int x = 0; x <= 10; x++) {
                System.out.print(y * x);
                System.out.print(" ");
            }
            System.out.println();
        }
    }
}
